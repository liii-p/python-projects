"""
Program which takes 3 float numbers and outputs the avg
"""

# inputs

floatNum1 = float(input("Enter your first float: "))
floatNum2 = float(input("Enter your second float: "))
floatNum3 = float(input("Enter your third float: "))

# calculation
avg =  (floatNum1 + floatNum2 + floatNum3) / 3

# output
print(avg)
