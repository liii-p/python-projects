# This Program adds 3 numbers
print("Please enter three numbers.")
num1 = int(input("First number: "))
num2 = int(input("Enter second number: "))
num3 = int(input("Enter third number: "))

print("The sum of these numbers is: ", (num1 + num2 + num3))

print("The subtraction of these numbers is: ", (num1 - num2 - num3))

print("The multiplication of these numbers is: ", (num1 * num2 * num3))

print("The division of these numbers is: ", (num1 + num2 + num3))
