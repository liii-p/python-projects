"""
This program will output the number 1 to 10 using a while loop
"""
# Pseudocode
# Program while_1_to_10
# OUTPUT HEADING
# num = 1
# WHILE num <= 10
#   OUTPUT num
#   num = num + 1
#  ENDWHILE
# END

print("While loop")

# num = 1
num = 10

while num <= 20:
    # num <= 10
    # num <= 100
    print(num)
    num = num + 1


